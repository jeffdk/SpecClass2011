/**
 * Performs a multiply add on a set of three numbers.  The first two numbers
 * are multiplied and summed with the third.
 *
 * @param mult1		the first value to be multiplied
 * @param mult2		the second value to be multiplied
 * @param add		the value to be added to the result of the multiplication
 * @return			the result of mult1 * mult2 + add
 */
double MultiplyAdd(const double mult1, const double mult2, const double add);
